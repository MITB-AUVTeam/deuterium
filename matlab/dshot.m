thrust = data(:,1);
dshot_val  = data(:,2);

%% ====== SORT ======
[thrust, idx] = sort(thrust);
dshot_val = dshot_val(idx);

%% ====== DEADZONE ======
deadband = 500; % <-- tune this (very important)

%% ====== SPLIT DATA ======
idx_neg = thrust < -deadband;
